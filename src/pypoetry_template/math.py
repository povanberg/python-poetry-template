r"""
The module contains the following functions:

- `mul(a, b)` - Returns the product of two numbers.
"""


def mul(a: int, b: int) -> int:
    """
    Multiply two integers

    Examples:
        >>> mul(2, 3)
        6

    Args:
        a (int): First integer
        b (int): Second integer

    Returns:
        int: product of the two parameter integers
    """
    return a * b
